//tugas looping While kedua
//looping dengan bilangan bulat loncat 2 dengan kalimat " I will become a mobile developer"
//tetapi dimulai dari angka 20 s/d 2

void main() {
  // Looping dengan bilangan bulat loncat 2 dari atas ke bawah
  var i = 20;

  while (i >= 2) {
    print("$i - I will become a mobile developer");
    i -= 2;
  }
}

