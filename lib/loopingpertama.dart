//tugas looping While pertama
//looping dengan bilangan bulat loncat 2 dengan kalimat " i love coding" dimulai dari angka 2 s/d 20

void main() {
  // Looping dengan bilangan bulat loncat 2
  var i = 2;

  while (i <= 20) {
    print("$i - I love coding");
    i += 2;
  }
}


